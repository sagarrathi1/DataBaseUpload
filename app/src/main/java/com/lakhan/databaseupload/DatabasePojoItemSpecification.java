package com.lakhan.databaseupload;

public class DatabasePojoItemSpecification {

    private String Colour,Description,Finish_Type,Material,Size,Type;

    public String getColour() {
        return Colour;
    }

    public void setColour(String colour) {
        Colour = colour;
    }

    public String getDescription() {
        return Description;
    }

    public void setDescription(String description) {
        Description = description;
    }

    public String getFinish_Type() {
        return Finish_Type;
    }

    public void setFinish_Type(String finish_Type) {
        Finish_Type = finish_Type;
    }

    public String getMaterial() {
        return Material;
    }

    public void setMaterial(String material) {
        Material = material;
    }

    public String getSize() {
        return Size;
    }

    public void setSize(String size) {
        Size = size;
    }

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }
}
